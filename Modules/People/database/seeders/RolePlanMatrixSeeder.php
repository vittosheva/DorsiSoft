    public function run(): void
    {
        app(PermissionRegistrar::class)->forgetCachedPermissions();

        $synchronized = 0;

        Company::query()
            ->select(['id'])
            ->with('currentSubscription:id,company_id,plan_code')
            ->chunkById(100, function ($companies) use (&$synchronized): void {
                foreach ($companies as $company) {
                    $plan = $company->currentSubscription?->plan_code?->value
                        ?? SubscriptionPlanEnum::Pro->value;

                    $synchronized += $this->countPermissionProvisioningRecords(
                        (int) $company->id,
                        function () use ($company, $plan): void {
                            $this->syncRoleMatrix((int) $company->id, $plan);
                        }
                    );
                }
            });

        setPermissionsTeamId(null);

        $this->reportSynchronized($synchronized);
    }

    protected function syncRoleMatrix(int $companyId, string $plan): void
    {
        $allowedPermissions = $this->permissionsByPlan($plan);

        setPermissionsTeamId($companyId);

        foreach ($this->rolePermissionTemplates() as $roleEnum => $permissions) {
            $role = Role::updateOrCreate(
                [
                    'name' => RoleEnum::from($roleEnum)->value,
                    'company_id' => $companyId,
                    'guard_name' => 'web',
                ],
                []
            );
            $role->syncPermissions(array_values(array_intersect($permissions, $allowedPermissions)));
        }
    }

